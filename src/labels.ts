export function resolveAmirielLabels(locale: "en" | "zh" = "en", labels: Partial<AmirielLabels> = {}): AmirielLabels {
  const base = AMIRIEL_DEFAULT_LABELS[locale] ?? AMIRIEL_DEFAULT_LABELS.en;
  return {
    ...base,
    ...labels,
    themes: { ...base.themes, ...labels.themes },
    fonts: { ...base.fonts, ...labels.fonts },
  };
}

export function formatAmirielLabel(template: string, values: Record<string, string | number>) {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? ""));
}
